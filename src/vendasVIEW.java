    private void carregarVendidos(){
        DefaultTableModel model = (DefaultTableModel) tabelaVendidos.getModel();
        model.setNumRows(0);
        
        try {
            ProdutosDAO dao = new ProdutosDAO();
            ArrayList<ProdutosDTO> vendidos = dao.listarProdutosVendidos();
        
            for (ProdutosDTO vendido : vendidos){
                model.addRow(new Object[]{
                    vendido.getId(),
                    vendido.getNome(),
                    vendido.getValor(),
                    vendido.getStatus()
                });
            }
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, "Erro ao carregar produtos vendidos: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

